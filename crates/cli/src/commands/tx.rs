#[derive(Subcommand)]
pub enum TxCommand {
    /// Get transaction by ID
    Get {
        /// The transaction id which is generated by rrelayer
        #[arg(long, short = 't')]
        tx_id: TransactionId,
    },
    /// Get transaction status by ID
    Status {
        /// The transaction id which is generated by rrelayer
        #[arg(long, short = 't')]
        tx_id: TransactionId,
    },
    /// List transactions for a relayer
    List {
        /// Relayer ID
        #[arg(long, short = 'r')]
        relayer_id: RelayerId,

        /// Filter by status (pending, sent, failed, success)
        #[arg(long)]
        status: Option<TxStatus>,

        /// Number of results to return (default: 10)
        #[arg(long, default_value = "10")]
        limit: u32,

        /// Number of results to skip (default: 0)
        #[arg(long, default_value = "0")]
        offset: u32,
    },
    /// List pending and mempool transactions size
    Queue {
        /// Relayer ID
        #[arg(long, short = 'r')]
        relayer_id: RelayerId,
    },
    /// Cancel a transaction
    Cancel {
        /// The transaction id which is generated by rrelayer
        #[arg(long, short = 't')]
        tx_id: TransactionId,
    },
    /// Replace a transaction
    Replace {
        /// The transaction id which is generated by rrelayer
        #[arg(long, short = 't')]
        tx_id: TransactionId,

        /// The transaction data as JSON
        #[arg(long, short = 'd')]
        transaction: RelayTransactionRequest,
    },
    /// Send a new transaction
    Send {
        /// Relayer ID
        #[arg(long, short = 'r')]
        relayer_id: RelayerId,

        /// The transaction data as JSON
        #[arg(long, short = 'd')]
        transaction: RelayTransactionRequest,
    },
    /// Withdraw tokens from the relayer to somewhere else
    Withdraw {
        /// Relayer ID
        #[arg(long, short = 'r')]
        relayer_id: RelayerId,

        /// Destination for the funds to go to
        #[arg(long)]
        to: EvmAddress,

        /// Amount to send (e.g., "1.5" for 1.5 ETH or "100" for 100 tokens)
        #[arg(long, short = 'a')]
        amount: String,

        /// The token address if you want an erc20/721 balance
        #[arg(long)]
        token: Option<EvmAddress>,

        /// Number of decimals for the token (default: 18 for ETH)
        #[arg(long, default_value = "18")]
        decimals: u8,
    },
    /// Fund tokens from a wallet to your relayer
    Fund {
        /// Relayer ID
        #[arg(long, short = 'r')]
        relayer_id: RelayerId,

        /// Amount to send (e.g., "1.5" for 1.5 ETH or "100" for 100 tokens)
        #[arg(long, short = 'a')]
        amount: String,

        /// The token address if you want an erc20/721 balance
        #[arg(long)]
        token: Option<EvmAddress>,

        /// Number of decimals for the token (default: 18 for ETH)
        #[arg(long, default_value = "18")]
        decimals: u8,
    },
}

#[derive(clap::ValueEnum, Clone)]
pub enum TxStatus {
    Pending,
    Sent,
    Failed,
    Success,
}

pub async fn handle_tx(command: &TxCommand, client: &Client) -> Result<(), TransactionError> {
    match command {
        TxCommand::Get { tx_id } => handle_get(tx_id, client).await,
        TxCommand::Withdraw { relayer_id, to, amount, token, decimals } => {
            let relayer_client = client.get_relayer_client(relayer_id, None).await?;
            handle_withdraw(to, amount, token, *decimals, relayer_client).await
        }
        TxCommand::Status { tx_id } => handle_status(tx_id, client).await,
        TxCommand::List { relayer_id, status, limit, offset } => {
            let relayer_client = client.get_relayer_client(relayer_id, None).await?;
            handle_list(status, *limit, *offset, relayer_client).await
        }
        TxCommand::Queue { relayer_id } => {
            let relayer_client = client.get_relayer_client(relayer_id, None).await?;
            handle_queue(relayer_client).await
        }
        TxCommand::Cancel { tx_id } => handle_cancel(tx_id, client).await,
        TxCommand::Replace { tx_id, transaction } => {
            handle_replace(tx_id, transaction, client).await
        }
        TxCommand::Send { relayer_id, transaction } => {
            let relayer_client = client.get_relayer_client(relayer_id, None).await?;
            handle_send(transaction, relayer_client).await
        }
        TxCommand::Fund { relayer_id, amount, token, decimals } => {
            handle_fund(relayer_id, amount, token, *decimals, client).await
        }
    }
}

async fn handle_get(tx_id: &TransactionId, client: &Client) -> Result<(), TransactionError> {
    let tx = client.transaction().get(tx_id).await?;
    if let Some(tx) = tx {
        log_transactions(vec![tx])?;
    } else {
        println!("Transaction {} not found.", tx_id);
    }
    Ok(())
}

async fn handle_withdraw(
    to: &EvmAddress,
    amount: &str,
    token: &Option<EvmAddress>,
    decimals: u8,
    client: AdminRelayerClient,
) -> Result<(), TransactionError> {
    let amount_wei = parse_units(amount, decimals)
        .map_err(|e| {
            TransactionError::CommandFailed(format!("Invalid amount format '{}': {}", amount, e))
        })?
        .into();

    println!(
        "Withdrawing {} {} (raw: {} wei)",
        amount,
        if token.is_some() { "tokens" } else { "ETH" },
        amount_wei
    );

    match token {
        Some(token_address) => {
            let call = IERC20::transferCall { to: (*to).into(), amount: amount_wei };

            let tx = client
                .transaction()
                .send(
                    &RelayTransactionRequest {
                        to: *token_address,
                        value: TransactionValue::zero(),
                        data: TransactionData::new(call.abi_encode().into()),
                        speed: Some(TransactionSpeed::FAST),
                        blobs: None,
                        external_id: None,
                    },
                    None,
                )
                .await?;

            println!("ERC-20 token withdrawal transaction sent..");
            println!("Transaction id: {}", tx.id);
            println!("Transaction hash: {}", tx.hash);
        }
        None => {
            let tx = client
                .transaction()
                .send(
                    &RelayTransactionRequest {
                        to: *to,
                        value: TransactionValue::new(amount_wei),
                        data: TransactionData::empty(),
                        speed: Some(TransactionSpeed::FAST),
                        blobs: None,
                        external_id: None,
                    },
                    None,
                )
                .await?;

            println!("ETH withdrawal transaction sent..");
            println!("Transaction id: {}", tx.id);
            println!("Transaction hash: {}", tx.hash);
        }
    }

    Ok(())
}

async fn handle_status(tx_id: &TransactionId, client: &Client) -> Result<(), TransactionError> {
    let status = client.transaction().get_status(tx_id).await?;
    match status {
        None => {
            println!("Can not find transaction id {}", tx_id)
        }
        Some(result) => {
            println!("Transaction status: {}", result.status);
            if let Some(hash) = result.hash {
                println!("Transaction hash: {}", hash);
            }
        }
    }

    Ok(())
}

